#include "libft.h"

int		ft_strncmp(const char *s1, const char *s2, size_t len)
{
	size_t		i;

	i = 0;
	if (i == len)
		return (0);
	while (i < len && s1[i] == s2[i] && s1[i] != '\0' && s1[i] != '\0')
		i++;
	return (s1[i] - s2[i]);
}
