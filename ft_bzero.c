#include "libft.h"

void	ft_bzero(void *s, size_t len)
{
	char	*ptr;
	size_t	i;

	ptr = s;
	i = 0;
	while (i < len)
	{
		*ptr = 0;
		ptr++;
		i++;
	}
}
